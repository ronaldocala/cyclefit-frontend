create table if not exists public.exercises (
  id bigint generated by default as identity primary key,
  slug text not null unique,
  name text not null,
  category text not null,
  equipment text[] not null default '{}'::text[],
  muscle_groups text[] not null default '{}'::text[],
  instructions text,
  is_active boolean not null default true
);

alter table public.exercises enable row level security;

grant select on public.exercises to authenticated;

drop policy if exists "exercises_select_active" on public.exercises;
create policy "exercises_select_active"
on public.exercises
for select
to authenticated
using (is_active = true);

create table if not exists public.user_workouts (
  id uuid primary key default gen_random_uuid(),
  user_id uuid not null references auth.users(id) on delete cascade,
  name text not null,
  description text,
  is_template boolean not null default false,
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now())
);

create index if not exists user_workouts_user_id_updated_at_idx on public.user_workouts (user_id, updated_at desc);

alter table public.user_workouts enable row level security;

grant select, insert, update, delete on public.user_workouts to authenticated;

drop policy if exists "user_workouts_select_own" on public.user_workouts;
create policy "user_workouts_select_own"
on public.user_workouts
for select
to authenticated
using (auth.uid() = user_id);

drop policy if exists "user_workouts_insert_own" on public.user_workouts;
create policy "user_workouts_insert_own"
on public.user_workouts
for insert
to authenticated
with check (auth.uid() = user_id);

drop policy if exists "user_workouts_update_own" on public.user_workouts;
create policy "user_workouts_update_own"
on public.user_workouts
for update
to authenticated
using (auth.uid() = user_id)
with check (auth.uid() = user_id);

drop policy if exists "user_workouts_delete_own" on public.user_workouts;
create policy "user_workouts_delete_own"
on public.user_workouts
for delete
to authenticated
using (auth.uid() = user_id);

create table if not exists public.workout_sessions (
  id uuid primary key default gen_random_uuid(),
  user_id uuid not null references auth.users(id) on delete cascade,
  user_workout_id uuid references public.user_workouts(id) on delete set null,
  source_type text not null check (source_type in ('user_workout', 'premium_workout', 'quick_log')),
  source_id text,
  status text not null default 'completed' check (status in ('started', 'completed', 'abandoned')),
  started_at timestamptz not null,
  completed_at timestamptz,
  duration_seconds integer check (duration_seconds is null or duration_seconds >= 0),
  rpe integer check (rpe is null or (rpe >= 1 and rpe <= 10)),
  notes text,
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now())
);

