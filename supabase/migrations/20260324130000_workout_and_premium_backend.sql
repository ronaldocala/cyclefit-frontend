create or replace function public.handle_user_workouts_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = timezone('utc', now());
  return new;
end;
$$;

drop trigger if exists set_user_workouts_updated_at on public.user_workouts;
create trigger set_user_workouts_updated_at
before update on public.user_workouts
for each row
execute function public.handle_user_workouts_updated_at();

